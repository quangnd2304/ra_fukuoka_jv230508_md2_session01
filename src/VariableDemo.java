public class VariableDemo {
    public static void main(String[] args) {
        // Syntax: Datatype variableName
        //Khai báo biến chứa thông tin mã sinh viên và tên sinh viên
        String studentId;
        String studentName;
        // Syntax: variableName = value
        // Khởi tạo giá trị cho các biến mã sinh viên là SV001 và tên sinh viên là Nguyễn Văn A
        studentId = "SV001";
        studentName = "Nguyễn Văn A";
        // Syntax: Datatype variableName = value
        // Số nguyên: byte, short, int, long
        // Số thực: float, double
        // Khai báo và khởi tạo giá trị biến tuổi sinh viên bằng 18
        short age = 18;
        // Khai báo và khởi tạo giá trị các điểm của sinh viên gồm toán = 7.5, lý = 9, hóa = 5
        //format code: Ctrl + Alt + L
        float toan = 7.5F, ly = 9, hoa = 5;
        //Khai báo và khởi tạo giá trị biến số nguyên a = 10 và biến số float b = 8.3
        int a = 10;
        float b = 8.3F;
        //Khai báo hằng số PI = 3.14
        final float PI = 3.14F;

    }
}
